# -*- coding: utf-8 -*-


def main():
    day = input()

    if day == 'Monday':
        print(5)
    elif day == 'Tuesday':
        print(4)
    elif day == 'Wednesday':
        print(3)
    elif day == 'Thursday':
        print(2)
    elif day == 'Friday':
        print(1)
    else:
        print(0)


if __name__ == '__main__':
    main()
