def main():
    n = str(input())
    digit = n[-1]

    if digit == '3':
        print('bon')
    elif digit == '0':
        print('pon')
    elif digit == '1':
        print('pon')
    elif digit == '6':
        print('pon')
    elif digit == '8':
        print('pon')
    else:
        print('hon')


if __name__ == '__main__':
    main()
